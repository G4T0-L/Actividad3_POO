    public double[] calculoSalario(double horas, double valor_hora, double porcentaje_retencion){
        double salario_bruto=horas*valor_hora;
        double salario_neto=salario_bruto-(salario_bruto*(porcentaje_retencion/100));
        
        double[] salarios={salario_bruto,salario_neto};
        
        return salarios;
    }
