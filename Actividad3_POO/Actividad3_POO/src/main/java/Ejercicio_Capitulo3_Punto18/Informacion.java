    private void buttonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCalcularActionPerformed
        try{
        double[] salarios = new double[2];
        salarios=calculoSalario(Double.parseDouble(textHoras.getText()),Double.parseDouble(textTrabajada.getText()),Double.parseDouble(textPorcentaje.getText()));
      
        JOptionPane.showMessageDialog(null,"Codigo del empleado: "+textCodigo.getText()+"\nNombres del empleado: "+textNombres.getText()+"\nSalario bruto del empleado: "+salarios[0]+"\nSalario neto del empleado: "+salarios[1]);
        }catch(NumberFormatException ex) {
                JOptionPane.showMessageDialog(null,"Ingresar solo caracteres numericos en los campos de Horas trabajadas,Valor hora, y Porcentaje");
            }
    }//GEN-LAST:event_buttonCalcularActionPerformed

